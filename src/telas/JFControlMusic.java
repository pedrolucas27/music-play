    private void btn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn1ActionPerformed
        String[] p = btn1.getIcon().toString().split("/");
        if (p[1].equals("pause_icon.png")) {
            img = new ImageIcon("imagens/play_icon.png");
            btn1.setIcon(img);
            pausar();
        } else {
            if (!flag) {
                img = new ImageIcon("imagens/pause_icon.png");
                btn1.setIcon(img);
                continuar();
            } else {
                img = new ImageIcon("imagens/pause_icon.png");
                btn1.setIcon(img);
                tocar(pathMusica);
            }

        }

    }//GEN-LAST:event_btn1ActionPerformed

