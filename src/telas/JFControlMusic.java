    public void tocar(String pathMusica) {
        m = new File(pathMusica);
        track = new Track(m);
        play = new Play(track);
        cp = new ControladorPlayer(play);
        cp.tocarMusica();
        cp.tocarMusica();
    }

    public void pausar() {
        cp.pausarMusica();
    }

    public void parar() {
        cp.pararMusica();
    }

    public void continuar() {
        cp.continuarMusica();
    }

