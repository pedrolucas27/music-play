    public void listarMusicas(ArrayList<Musica> list){
        String colunas = "ID:\t"+"NOME:\t"+"DURAÇÃO:\t"+"GÊNERO:\n";
        jtxLista.setText(colunas);
        for(int i=0; i < list.size(); i++){
            String linha = list.get(i).getId()+"\t"+list.get(i).getTitulo()+"\t"+
                    list.get(i).getDuracao()+"\t"+list.get(i).getGenero().getNome()+"\n";
            jcbMusicas.addItem(String.valueOf(list.get(i).getId()));
            jtxLista.append(linha);
        }
        jtxLista.setEnabled(false);
    }
    

