    private void btnDuracaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDuracaoActionPerformed
        tempo = JFPlayList.pl.calcularDuracao();
        String formatacao = null;
        if(tempo > 1440){
           int d = tempo/1440; 
           int h = (tempo - (d*1440))/60;
           int m = tempo - (((tempo - (d*1440))/60)*60); 
           formatacao = d+" dias,"+h+" horas,"+m+" minutos";
        }else{
           int h = tempo/60;
           int m = tempo - (h*60); 
           int s = (tempo * 60) - ((h*60*60)+(m*60));
           formatacao = 0+" dias,"+h+" horas,"+m+" minutos"; 
        }
        
        duracaoTxt.setText(formatacao);
        duracaoTxt.setVisible(true);
    }//GEN-LAST:event_btnDuracaoActionPerformed

    private void btnTocarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTocarActionPerformed
        int index = jcbMusicas.getSelectedIndex();
        
        if(index != 0){
            Musica musica = musicas.get(index - 1);
            setVisible(false);
            new JFControlMusic(musica).setVisible(true);
        }else{
           JOptionPane.showMessageDialog(this, "SELECIONE UMA MÚSICA !");
        }
        
    }//GEN-LAST:event_btnTocarActionPerformed

